"""Static wordlist used by ``add.py`` to generate human-friendly slugs.

The slug is the ``<slug>`` segment of
``changelog/@unreleased/pr-<N>-<slug>.yml``. We pick two random words
from this list and join with ``-`` (so the resulting filename always
matches the lint's ``[A-Za-z0-9_-]+`` pattern).

Constraints on every entry:

- Lowercase ``a-z`` only — no apostrophes, digits, or hyphens. The
  filename pattern does not forbid those, but keeping the list strict
  avoids surprises and makes the rendered filename copy-paste-safe in
  shells that quote special characters.
- 3-7 characters per word — short enough that ``<word>-<word>``
  filenames stay under 20 characters of slug.
- No homophones for sensitive operational terms (``crash``, ``leak``,
  ``hack``, ``attack``, …). The slug is purely cosmetic; an unrelated
  PR named ``pr-1234-leak-bottle.yml`` would be misleading.
- Hand-curated subset of the EFF Short Wordlist 1.0
  (https://www.eff.org/dice — public domain, no attribution required).

With ~200 words the two-word combination space is ~40 000, which is
several orders of magnitude over what one PR is ever going to need.
``add.py`` retries on filename collisions inside one PR's directory
to handle the rare clash.

The list is intentionally a flat module constant rather than a JSON
file so it round-trips through type-checkers and avoids a runtime
file read.
"""

